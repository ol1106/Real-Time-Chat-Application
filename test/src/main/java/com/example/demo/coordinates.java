package com.example.demo;

public class coordinates {
    Integer cord1 ;
    Integer cord2 ;

    public coordinates(Integer cord1, Integer cord2) {
        this.cord1 = cord1;
        this.cord2 = cord2;
    }

    public Integer getCord1() {
        return cord1;
    }

    public void setCord1(Integer cord1) {
        this.cord1 = cord1;
    }

    public Integer getCord2() {
        return cord2;
    }

    public void setCord2(Integer cord2) {
        this.cord2 = cord2;
    }
}
